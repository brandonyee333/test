import {expect, mergeTests} from '@playwright/test';

import {partnerHelperTest} from '../../../fixtures/partnerHelperTest';
import {partnerPagesTest} from '../../../fixtures/partnerPagesTest';
import {partnerSiteTest} from '../../../fixtures/partnerSiteTest';
import {customFormatDate} from '../../../utils/customFormatDate';
import getDateCustomFormat from '../../../utils/getFormatDate';
import {mdfRequestGet} from '../../../mocks/mdfMocks';

export const test = mergeTests(partnerHelperTest, partnerSiteTest, partnerPagesTest);

test.describe('MDF Request Page', () => {
	const accountName = 'Deathray, Inc.*';
	const accountRole = '[Account] Partner Manager (PM)';

	let accountUser;
	let autoGenerateData;
	let formatEndDate;
	let formatStartDate;
	let mdfRequestData;

	test.beforeEach(async ({partnerHelper, mdfRequestListPage}) => {
		const { account } = await partnerHelper.createAccountUser({accountName, accountType: 'business'});
		await partnerHelper.assignUserToAccountRole({accountId: account.id, accountRole});

		accountUser = account;

		mdfRequestData = await partnerHelper.createMDFRequest({
			data: mdfRequestGet
		});

		await mdfRequestListPage.goto();

		autoGenerateData = await mdfRequestListPage.getGeneratedDataFromRequest(
			mdfRequestData.overallCampaignName
		);

		formatEndDate = getDateCustomFormat(
			mdfRequestData.maxDateActivity,
			customFormatDate.SHORT_MONTH
		);
		formatStartDate = getDateCustomFormat(
			mdfRequestData.minDateActivity,
			customFormatDate.SHORT_MONTH
		);
	});

	test.afterEach(async ({partnerHelper}) => {
		await partnerHelper.apiHelpers.headlessAdminUser.deleteAccount(accountUser.id);

		await partnerHelper.apiHelpers.delete(`/o/c/mdfrequests/${mdfRequestData.id}`);
	})

	test('Should display MDF Resquest List', async ({mdfRequestListPage}) => {
		const campaignName = await mdfRequestListPage.getCampaignName();
		const endActPeriod =
			await mdfRequestListPage.getEndActPeriod(formatEndDate);
		const partnerName = await mdfRequestListPage.getPartnerName(
			mdfRequestData.companyName
		);
		const requested = await mdfRequestListPage.getRequested(
			mdfRequestData.totalMDFRequestAmount
		);
		const startActPeriod =
			await mdfRequestListPage.getStartActPeriod(formatStartDate);

		await expect(campaignName).toBeVisible();
		await expect(autoGenerateData.claimed).toBeTruthy();
		await expect(endActPeriod).toBeVisible();
		await expect(partnerName).toBeVisible();
		await expect(requested).toBeTruthy();
		await expect(autoGenerateData.requestId).toBeTruthy();
		await expect(startActPeriod).toBeVisible();
		await expect(autoGenerateData.status).toBeTruthy();
		await expect(autoGenerateData.submitDate).toBeTruthy();
	});

	test('Should filter data by Activity Period', async ({
		page,
		mdfRequestListPage,
	}) => {
		const endActPeriod =
			await mdfRequestListPage.getEndActPeriod(formatEndDate);
		const startActPeriod =
			await mdfRequestListPage.getStartActPeriod(formatStartDate);

		await mdfRequestListPage.filterMDFRequestByPeriod(
			'2024-07-11',
			'2024-07-12'
		);

		await page.getByText('MDF Requests').click();

		await expect(endActPeriod).toBeVisible();
		await expect(startActPeriod).toBeVisible();

		await mdfRequestListPage.clearAllFilters.click();
		await mdfRequestListPage.filterButton.click();

		await mdfRequestListPage.activityAfterDateInput.fill('2024-08-09');
		await mdfRequestListPage.activityBeforeDateInput.fill('2024-08-10');

		await mdfRequestListPage.applyFilterButton.click();
		await page.getByText('MDF Requests').click();

		await expect(mdfRequestListPage.noEntriesFoundMessage).toBeVisible();
		await expect(endActPeriod).not.toBeVisible();
		await expect(startActPeriod).not.toBeVisible();
	});

	test('Should filter data by Status', async ({page, mdfRequestListPage}) => {
		const status = autoGenerateData.status;

		await mdfRequestListPage.filterMDFRequestByStatus(status);

		await mdfRequestListPage.mdfRequestHeading.click();

		await expect(status).toBeTruthy();

		await mdfRequestListPage.clearAllFilters.click();
		await mdfRequestListPage.filterButton.click();

		await page.getByLabel('Draft').check();

		await mdfRequestListPage.applyFilterButton.click();
		await mdfRequestListPage.mdfRequestHeading.click();

		await expect(mdfRequestListPage.noEntriesFoundMessage).toBeVisible();
	});

	test('Should filter data by Partner', async ({
		page,
		partnerHelper,
		mdfRequestListPage,
	}) => {
		const { account } = await partnerHelper.createAccountUser({accountName: 'Company Name 3', accountType: 'business'});
		await partnerHelper.assignUserToAccountRole({accountId: account.id, accountRole});

		const partnerName = await mdfRequestListPage.getPartnerName(
			mdfRequestData.companyName
		);

		await mdfRequestListPage.filterMDFRequestByPartner(
			mdfRequestData.companyName
		);

		await page.getByText('MDF Requests').click();

		await expect(partnerName).toBeVisible();

		await mdfRequestListPage.clearAllFilters.click();
		await mdfRequestListPage.filterButton.click();

		await page.getByLabel('Company Name 3').check();

		await mdfRequestListPage.applyFilterButton.click();
		await page.getByText('MDF Requests').click();

		await expect(mdfRequestListPage.noEntriesFoundMessage).toBeVisible();
	});

	test('Should clean date filter fields when click on Clear All Filters', async ({
		mdfRequestListPage,
	}) => {
		await mdfRequestListPage.filterMDFRequestByPeriod(
			'2024-06-01',
			'2024-06-08'
		);

		await mdfRequestListPage.clearAllFilters.click();
		await mdfRequestListPage.filterButton.click();

		await expect(mdfRequestListPage.activityAfterDateInput).toBeEmpty();
		await expect(mdfRequestListPage.activityBeforeDateInput).toBeEmpty();
	});

	test('Should Download MDF Report', async ({page, mdfRequestListPage}) => {
		const downloadPromise = page.waitForEvent('download');
		await mdfRequestListPage.exportRequestButton.click();

		const downloadMDFReport = await downloadPromise;

		await downloadMDFReport.saveAs(
			'~/' + downloadMDFReport.suggestedFilename()
		);
		expect(downloadMDFReport.suggestedFilename()).toBe('MDF Requests.csv');
	});

	test('Should Change MDF Request Status', async ({
		mdfRequestFormPage,
		mdfRequestListPage,
	}) => {
		const requestId = await mdfRequestListPage.getRequestId(
			autoGenerateData.requestId
		);

		await requestId.click();

		await mdfRequestFormPage.statusDropdown.click();
		await mdfRequestFormPage.statusDropDownOption('Approved');

		await mdfRequestFormPage.backButton.click();

		await expect(mdfRequestListPage.getStatus('Approved')).toBeTruthy();
	});

	test('Should Find a Request using the search input', async ({
		mdfRequestListPage,
	}) => {
		const campaignName = await mdfRequestListPage.getCampaignName();
		const cleanSearch = await mdfRequestListPage.cleanSearch;

		await mdfRequestListPage.filterUsingSearchInput('Test Campaign');

		await expect(campaignName).toBeVisible();

		await cleanSearch.click();
		await mdfRequestListPage.filterUsingSearchInput('Name');

		await expect(mdfRequestListPage.noEntriesFoundMessage).toBeVisible();
	});

	test('Should Complete A MDF Request through action button', async ({
		page,
		mdfRequestListPage,
		mdfRequestFormPage
	}) => {
		const actionButton = await mdfRequestListPage.actionButton;
		const completedTab = await mdfRequestListPage.completedTab;
		const completeMenuItem = await mdfRequestListPage.completeMenuItem;
		const requestId = await mdfRequestListPage.getRequestId(
			autoGenerateData.requestId
		);
		const successTooltip = await page.getByText('Success:MDF Request successfully completed!');

		await requestId.click();

		await mdfRequestFormPage.statusDropdown.click();
		await mdfRequestFormPage.statusDropDownOption('Approved');

		await mdfRequestFormPage.backButton.click();

		await page.once('dialog', async (dialog) => {
			expect(dialog.message()).toContain('Are you sure you want to complete the MDF request?');
			await dialog.accept();
		});

		await actionButton.click();
		await completeMenuItem.click();

		await expect(successTooltip).toBeVisible();

		await completedTab.click();

		await expect(requestId).toBeVisible();
	});
});
