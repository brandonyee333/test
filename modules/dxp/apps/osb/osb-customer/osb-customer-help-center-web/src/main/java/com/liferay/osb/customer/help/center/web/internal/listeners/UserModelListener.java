package com.liferay.osb.customer.help.center.web.internal.listeners;

import com.liferay.osb.customer.help.center.web.internal.util.HelpCenterThemeUtil;
import com.liferay.portal.kernel.exception.ModelListenerException;
import com.liferay.portal.kernel.model.BaseModelListener;
import com.liferay.portal.kernel.model.ModelListener;
import com.liferay.portal.kernel.model.Organization;
import com.liferay.portal.kernel.model.User;

import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Amos Fong
 */
@Component(immediate = true, service = ModelListener.class)
public class UserModelListener extends BaseModelListener<User> {

	@Override
	public void onAfterAddAssociation(
			Object classPK, String associationClassName,
			Object associationClassPK)
		throws ModelListenerException {

		if (associationClassName.equals(Organization.class.getName())) {
			_helpCenterThemeUtil.clearCache();
		}
	}

	@Override
	public void onAfterRemoveAssociation(
			Object classPK, String associationClassName,
			Object associationClassPK)
		throws ModelListenerException {

		if (associationClassName.equals(Organization.class.getName())) {
			_helpCenterThemeUtil.clearCache();
		}
	}

	@Reference
	private HelpCenterThemeUtil _helpCenterThemeUtil;

}