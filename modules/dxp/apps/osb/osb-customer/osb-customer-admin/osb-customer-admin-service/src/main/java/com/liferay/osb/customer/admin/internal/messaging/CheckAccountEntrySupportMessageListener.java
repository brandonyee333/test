import com.liferay.portal.kernel.scheduler.TriggerFactory;

import java.util.List;
import java.util.Map;

import org.osgi.service.component.annotations.Activate;
import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Deactivate;
import org.osgi.service.component.annotations.Modified;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Amos Fong
 */
@Component(
	immediate = true, service = CheckAccountEntrySupportMessageListener.class
)
public class CheckAccountEntrySupportMessageListener
	extends BaseMessageListener {

	@Activate
	@Modified
	protected void activate(Map<String, Object> properties) {
		Class<?> clazz = getClass();

		String className = clazz.getName();

		Trigger trigger = _triggerFactory.createTrigger(
			className, className, null, null, 1, TimeUnit.DAY);

		SchedulerEntry schedulerEntry = new SchedulerEntryImpl(
			className, trigger);

		_schedulerEngineHelper.register(
			this, schedulerEntry, DestinationNames.SCHEDULER_DISPATCH);
	}

	@Deactivate
	protected void deactivate() {
		_schedulerEngineHelper.unregister(this);
	}

	@Override
	protected void doReceive(Message message) throws Exception {
		List<AccountEntry> accountEntries =
			_accountEntryLocalService.getExpiredSupportAccountEntries(
				QueryUtil.ALL_POS, QueryUtil.ALL_POS);

		for (AccountEntry accountEntry : accountEntries) {
			Account account = _accountWebService.getAccount(
				accountEntry.getKoroneikiAccountKey());

			List<ProductPurchase> productPurchases =
				_accountReader.getProductPurchases(account.getKey());

			_accountEntryLocalService.updateAccountEntry(
				accountEntry.getAccountEntryId(),
				_accountReader.getSupportEndDate(productPurchases),
				_accountReader.getTicketSupportEndDate(productPurchases),
				_accountReader.getStatus(account));
		}
	}

	@Reference
	private AccountEntryLocalService _accountEntryLocalService;

	@Reference
	private AccountReader _accountReader;

	@Reference
	private AccountWebService _accountWebService;

	@Reference
	private SchedulerEngineHelper _schedulerEngineHelper;

	@Reference
	private TriggerFactory _triggerFactory;

}