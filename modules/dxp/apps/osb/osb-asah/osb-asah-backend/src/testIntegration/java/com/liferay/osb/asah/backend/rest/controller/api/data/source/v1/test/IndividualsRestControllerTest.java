package com.liferay.osb.asah.backend.rest.controller.api.data.source.v1.test;

import com.liferay.osb.asah.backend.spring.OSBAsahBackendSpringBootApplication;
import com.liferay.osb.asah.common.wedeploy.data.WeDeployDataService;
import com.liferay.osb.asah.test.util.elasticsearch.ElasticsearchIndex;
import com.liferay.osb.asah.test.util.spring.OSBAsahSpringJUnit4ClassRunner;

import io.restassured.http.Method;
import io.restassured.response.ValidatableResponse;

import org.hamcrest.Matchers;

import org.junit.Test;
import org.junit.runner.RunWith;

import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.context.ContextConfiguration;

/**
 * @author Vishal Reddy
 */
@ContextConfiguration(classes = OSBAsahBackendSpringBootApplication.class)
@RunWith(OSBAsahSpringJUnit4ClassRunner.class)
@SpringBootTest(webEnvironment = SpringBootTest.WebEnvironment.RANDOM_PORT)
public class IndividualsRestControllerTest extends BaseRestControllerTestCase {

	/**
	 * The JSON files are named after the WeDeploy collections that they are
	 * populated into. The JSON files were generated by running the
	 * DXPIndividualsNanite test, and retrieved from the WeDeploy data container
	 * before the tests cleaned up the data.
	 */
	@ElasticsearchIndex(
		name = "data-sources", resourcePath = "data_sources.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "field-mappings", resourcePath = "field_mappings.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "fields", resourcePath = "fields.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "individual-segments", resourcePath = "individual_segments.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "users", resourcePath = "users.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_DXP_RAW
	)
	@Test
	public void testGetIndividuals() {
		ValidatableResponse validatableResponse = getValidatableResponse(
			Method.GET, "/api/1.0/individuals");

		validateIndividuals(validatableResponse);
	}

	@ElasticsearchIndex(
		name = "accounts", resourcePath = "accounts.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "individual-segments", resourcePath = "individual_segments.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@ElasticsearchIndex(
		name = "individuals", resourcePath = "individuals.json",
		weDeployDataService = WeDeployDataService.OSB_ASAH_FARO_INFO
	)
	@Test
	public void testGetIndividualsWithFilter() {

		// Lower case to upper case conversion

		_validateTotalElements(
			"((contains(demographics/givenName/value,'test')))", 1);

		// Upper case to lower case conversion

		_validateTotalElements(
			"((contains(demographics/givenName/value,'TEST')))", 1);

		// Mixed case

		_validateTotalElements(
			"((contains(demographics/givenName/value,'tEsT')))", 1);

		// Characters out of order

		_validateTotalElements(
			"((contains(demographics/givenName/value,'tset')))", 0);

		// Subsequence missing interior characters

		_validateTotalElements(
			"((contains(demographics/givenName/value,'tet')))", 0);

		// Symbols with mixed case

		_validateTotalElements(
			"((contains(demographics/email/value,'.teSt@L')))", 1);

		// Wrong symbol

		_validateTotalElements(
			"((contains(demographics/email/value,'>test@l')))", 0);

		// Regex symbols escaped

		_validateTotalElements(
			"((contains(demographics/email/value,'.*')))", 0);

		// Individuals with a specific account

		_validateTotalElements("((accountId eq '342313458385210529'))", 0);

		_validateTotalElements("((accountId eq '342313459339515838'))", 1);

		_validateTotalElements(
			"((dataSourceAccountPKs/accountPKs eq '0016C00000BKWWKQA5'))", 0);

		_validateTotalElements(
			"((dataSourceAccountPKs/accountPKs eq '0016C00000BKWWJQA5'))", 1);

		// Individuals with any accounts

		_validateTotalElements("((accountId ne null))", 1);

		_validateTotalElements(
			"((dataSourceAccountPKs/accountPKs ne null))", 1);

		// Individuals without any accounts

		_validateTotalElements("((accountId eq null))", 1);

		_validateTotalElements(
			"((dataSourceAccountPKs/accountPKs eq null))", 1);

		// Individuals in a specific data source

		_validateTotalElements("((dataSourceId eq '337984445922213329'))", 2);

		_validateTotalElements("((dataSourceId eq '337984445922213328'))", 0);

		// Individuals with a specific individual PK

		_validateTotalElements(
			"((dataSourceIndividualPKs/individualPKs eq " +
				"'953be104-5540-abf8-59b8-55f895200acc'))",
			1);
	}

	private void _validateTotalElements(String filterString, int elements) {
		ValidatableResponse validatableResponse = getValidatableResponse(
			Method.GET, "/api/1.0/individuals?filter=" + filterString);

		validateResponse(
			Matchers.is(elements), "page.totalElements", validatableResponse);
	}

}